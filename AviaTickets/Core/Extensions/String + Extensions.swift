import Foundation

extension String {
    
    public static var empty: String {
        return ""
    }
    
    public static var space: String {
        return " "
    }
    
    public static var dash: String {
        return "-"
    }
    
    public static var dot: String {
        return "."
    }
    
    public static var slash: String {
        return "/"
    }
    
}
